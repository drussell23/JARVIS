# ---------------------------------------------------------------------------
# Public enums
# ---------------------------------------------------------------------------


class InlineDecision(str, enum.Enum):
    """Verdict returned by :func:`decide`."""

    SAFE = "safe"
    ASK = "ask"
    BLOCK = "block"


class RoutePosture(str, enum.Enum):
    """Collapsed view of :class:`ProviderRoute` relevant to prompting.

    We avoid importing :class:`ProviderRoute` directly to keep this
    primitive circular-import-safe and to document that the *only* route
    property the gate cares about is "is there a human who could answer
    an inline prompt?".
    """

    INTERACTIVE = "interactive"
    """Human may be on the console (IMMEDIATE / STANDARD / COMPLEX)."""

    AUTONOMOUS = "autonomous"
    """No human present (BACKGROUND / SPECULATIVE). ASK→BLOCK coercion."""


class UpstreamPolicy(str, enum.Enum):
    """Result from the upstream :class:`PolicyEngine` (or equivalent)."""

    BLOCKED = "blocked"
    APPROVAL_REQUIRED = "approval_required"
    SAFE_AUTO = "safe_auto"
    NO_MATCH = "no_match"


# ---------------------------------------------------------------------------
# Input / verdict dataclasses
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class OpApprovedScope:
    """Structured operator/data-plane facts about what the op-level gate blessed.

    Supplied by the orchestration layer — *never* sourced from model
    output. Model rationales are display-only; authorization inputs are
    the caller's responsibility (§1, §7).
    """

    approved_paths: Tuple[str, ...] = ()
    """Repo-relative POSIX paths (files or directories) the op-level gate
    blessed. A target is "in scope" if it exactly equals one of these or
    is nested under one of them."""

    candidate_hash: str = ""
    """Fingerprint of the candidate the op-level gate actually saw.
    Slice 2+ uses this to detect tool-call-time divergence from the
    blessed candidate."""

    is_read_only: bool = False
    """When True, the enclosing op was declared read-only
    (``OperationContext.is_read_only``). Mirrors ToolExecutor's existing
    ``PolicyContext.is_read_only`` convention."""


@dataclass(frozen=True)
class InlineGateInput:
    """Explicit, structured inputs to a per-tool-call decision.

    All fields must be sourced from orchestration code or the operator,
    never from model claims about "why this is safe".
    """

    tool: str
    """Tool name, e.g. ``"edit_file"`` / ``"bash"`` / ``"read_file"``."""

    arg_fingerprint: str = ""
    """Stable deterministic summary of arguments.

    For ``bash``: the command line. For file-scoped tools: typically the
    target path (the caller may pass ``""``; the gate will fall back to
    ``target_path``). Remembered-allow lookup matches against this."""

    target_path: str = ""
    """Repo-relative POSIX path when the call is file-scoped; ``""`` otherwise.

    Non-empty paths that begin with ``/`` or contain a ``..`` segment are
    classified as ``outside_repo``."""

    route: RoutePosture = RoutePosture.INTERACTIVE
    """§7 fail-closed driver: AUTONOMOUS coerces ASK→BLOCK."""

    approved_scope: OpApprovedScope = field(default_factory=OpApprovedScope)

    upstream_decision: UpstreamPolicy = UpstreamPolicy.NO_MATCH
    """Result of the upstream :class:`PolicyEngine`. BLOCKED mirrors through."""


@dataclass(frozen=True)
class InlineGateVerdict:
    """Verdict returned by :func:`decide`."""

    decision: InlineDecision
    rule_id: str
    reason: str
    ruleset_version: str = INLINE_PERMISSION_RULESET_VERSION


