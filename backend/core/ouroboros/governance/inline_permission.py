def ruleset() -> Tuple[InlineRule, ...]:
    """Return the active v0 ruleset (immutable)."""
    return _RULESET


# ---------------------------------------------------------------------------
# decide() — the single public entry point
# ---------------------------------------------------------------------------


_UPSTREAM_BLOCK_VERDICT = InlineGateVerdict(
    decision=InlineDecision.BLOCK,
    rule_id="RULE_UPSTREAM_BLOCKED",
    reason="PolicyEngine upstream decision is BLOCKED; inline gate mirrors",
)


def decide(
    inp: InlineGateInput,
    *,
    remembered: Optional[RememberedAllowProvider] = None,
) -> InlineGateVerdict:
    """Classify a proposed tool call.

    Order of evaluation (§6 "additive, never weakens"):

    1. If ``inp.upstream_decision`` is BLOCKED → mirror BLOCK.
    2. Walk :data:`_RULESET` in order. The first matching row wins for
       BLOCK. For SAFE rows, remembered-allow is consulted first so that
       operator-remembered patterns bypass *only* SAFE/ASK territory —
       never BLOCK.
    3. If a matched row is ASK and ``inp.route`` is AUTONOMOUS, coerce
       to BLOCK with a rule_id prefix of ``"autonomous_coerce:"`` so
       telemetry can distinguish prompt-suppression-fail-closed from a
       hard-BLOCK row.

    Returns
    -------
    InlineGateVerdict
        ``decision`` is SAFE / ASK / BLOCK. ``rule_id`` identifies the
        row that fired; ``ruleset_version`` pins
        :data:`INLINE_PERMISSION_RULESET_VERSION` for §8 postmortems.
    """
    if inp.upstream_decision is UpstreamPolicy.BLOCKED:
        return _UPSTREAM_BLOCK_VERDICT

    # First pass: look for a BLOCK match. BLOCKs always win over remembered-allow.
    for rule in _RULESET:
        if rule.decision is InlineDecision.BLOCK and rule.matcher(inp):
            return InlineGateVerdict(
                decision=InlineDecision.BLOCK,
                rule_id=rule.rule_id,
                reason=rule.reason,
            )

    # Second pass: remembered-allow (SAFE-scope only).
    if remembered is not None:
        try:
            if remembered.is_pattern_remembered(
                tool=inp.tool,
                arg_fingerprint=inp.arg_fingerprint or inp.target_path,
                target_path=inp.target_path,
            ):
                return InlineGateVerdict(
                    decision=InlineDecision.SAFE,
                    rule_id="RULE_REMEMBERED_ALLOW",
                    reason="operator-remembered allow pattern (Slice 3 store)",
                )
        except Exception as exc:  # noqa: BLE001
            # A broken provider must never escalate privilege. Log and ignore.
            logger.warning(
                "RememberedAllowProvider raised; ignoring: %s", exc,
            )

    # Third pass: SAFE/ASK rows in declaration order.
    for rule in _RULESET:
        if rule.decision is InlineDecision.BLOCK:
            continue
        if not rule.matcher(inp):
            continue
        if rule.decision is InlineDecision.ASK and inp.route is RoutePosture.AUTONOMOUS:
            return InlineGateVerdict(
                decision=InlineDecision.BLOCK,
                rule_id=f"autonomous_coerce:{rule.rule_id}",
                reason=(
                    "autonomous route cannot render an inline prompt; "
                    f"original ASK reason: {rule.reason}"
                ),
            )
        return InlineGateVerdict(
            decision=rule.decision,
            rule_id=rule.rule_id,
            reason=rule.reason,
        )

    # Cannot happen — RULE_FALLTHROUGH always matches. Defensive fallback.
    return InlineGateVerdict(
        decision=InlineDecision.BLOCK
        if inp.route is RoutePosture.AUTONOMOUS
        else InlineDecision.ASK,
        rule_id="RULE_FALLTHROUGH_DEFENSIVE",
        reason="ruleset exhausted without match (should not occur)",
    )


# ---------------------------------------------------------------------------
# InlinePermissionGate — thin object wrapper (injectable for tests / Slice 2)
# ---------------------------------------------------------------------------


class InlinePermissionGate:
    """Thin object wrapper around :func:`decide`.

    Slice 2 will inject an instance of this into the tool-executor hook
    point. Slice 1 keeps the object boundary narrow — ruleset is frozen
    module-level, :class:`RememberedAllowProvider` is injected, decide
    is a pass-through. No hidden state.
    """

    def __init__(
        self,
        *,
        remembered: Optional[RememberedAllowProvider] = None,
    ) -> None:
        self._remembered = remembered

    def classify(self, inp: InlineGateInput) -> InlineGateVerdict:
        return decide(inp, remembered=self._remembered)
