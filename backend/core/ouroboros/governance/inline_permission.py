@dataclass(frozen=True)
class InlineRule:
    """A single deterministic row of the v0 ruleset.

    ``matcher`` takes an :class:`InlineGateInput` and returns True iff the
    row fires. Rows are evaluated in declaration order (first-match-wins
    within each decision bucket; see :func:`decide` for the outer order).
    """

    rule_id: str
    decision: InlineDecision
    reason: str
    matcher: Callable[[InlineGateInput], bool]


def _is_bash(inp: InlineGateInput) -> bool:
    return inp.tool == "bash"


def _bash_cmd(inp: InlineGateInput) -> str:
    return inp.arg_fingerprint if _is_bash(inp) else ""


def _always_match(inp: InlineGateInput) -> bool:
    """Terminal fallthrough matcher — always fires (referenced by RULE_FALLTHROUGH)."""
    _ = inp  # structural: v0 matcher signature
    return True


