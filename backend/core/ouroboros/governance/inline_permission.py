@runtime_checkable
class RememberedAllowProvider(Protocol):
    """Read-only lookup of operator-remembered allow patterns.

    Slice 1 contract (do not widen in this slice):
        * lookup is read-only and side-effect-free on the gate's path;
        * remembered-allow never overrides a BLOCK rule (only SAFE-ish
          shapes are eligible);
        * persistence / write / expiry / scoping lives in Slice 3.
    """

    def is_pattern_remembered(
        self,
        *,
        tool: str,
        arg_fingerprint: str,
        target_path: str,
    ) -> bool: ...


# ---------------------------------------------------------------------------
# Protected path substrings (mirror of tool_executor._PROTECTED_PATH_SUBSTRINGS
# — kept independent to keep this primitive import-free of the monster module)
# ---------------------------------------------------------------------------

_PROTECTED_PATH_SUBSTRINGS: Tuple[str, ...] = (
    ".git/", "/.git", ".env", "credentials", "secret",
    "node_modules/", ".venv/", "venv/",
    ".ssh/", "id_rsa", "id_ed25519", ".aws/", ".gcp/",
    ".pypirc", ".netrc", ".jarvis/", ".ouroboros/",
)

# Dangerous absolute paths — ``rm -rf`` on any of these is fatal to the host.
_SYSTEM_ROOTS: Tuple[str, ...] = (
    "/", "/*", "~", "$HOME",
    "/bin", "/boot", "/dev", "/etc", "/home", "/lib", "/lib64",
    "/opt", "/proc", "/root", "/sbin", "/srv", "/sys", "/usr", "/var",
)


# ---------------------------------------------------------------------------
# Read-only tool whitelist (file-reading / search / git-inspection family)
# ---------------------------------------------------------------------------

_READ_ONLY_TOOLS: frozenset = frozenset({
    "read_file", "search_code", "get_callers", "glob_files",
    "list_dir", "list_symbols", "git_log", "git_diff", "git_blame",
    "code_explore",
})


# ---------------------------------------------------------------------------
# Bash regexes — compiled once at import time (§5 "<1ms" budget)
# ---------------------------------------------------------------------------

_BASH_SUDO_RX = re.compile(r"(?:^|[\s;&|])sudo\b", re.IGNORECASE)

# curl … | sh / wget … | bash (pipe-to-shell remote-exec)
_BASH_CURL_PIPE_SH_RX = re.compile(
    r"\b(?:curl|wget)\b[^|;&\n]*\|\s*(?:sh|bash|zsh|ksh)\b",
    re.IGNORECASE,
)

# rm -rf in any flag-permutation (-rf / -fr / -rfv / --recursive --force / -r -f / -f -r)
_BASH_RM_RF_RX = re.compile(
    r"\brm\b(?=\s)"
    r"(?:"
    r"\s+[^\s;|&]*(?:-[a-zA-Z]*[rR][a-zA-Z]*[fF][a-zA-Z]*|"
    r"-[a-zA-Z]*[fF][a-zA-Z]*[rR][a-zA-Z]*)"
    r"|"
    r"(?:\s+\S+)*\s+-[rR]\s+-[fF]\b"
    r"|"
    r"(?:\s+\S+)*\s+-[fF]\s+-[rR]\b"
    r"|"
    r"(?:\s+\S+)*\s+--recursive\b(?:\s+\S+)*\s+--force\b"
    r"|"
    r"(?:\s+\S+)*\s+--force\b(?:\s+\S+)*\s+--recursive\b"
    r")",
    re.IGNORECASE,
)

# dd … of=/dev/…  (block-device overwrite)
_BASH_DD_DEVICE_RX = re.compile(r"\bdd\b[^;|&\n]*\bof=/dev/", re.IGNORECASE)

# mkfs / mkfs.ext4 / mkfs.xfs / …
_BASH_MKFS_RX = re.compile(r"\bmkfs(?:\.[a-z0-9]+)?\b", re.IGNORECASE)

# Classic bash fork bomb  :(){ :|:& };:
_BASH_FORK_BOMB_RX = re.compile(r":\(\)\s*\{\s*:\s*\|\s*:\s*&\s*\}\s*;\s*:")

# chmod 777 / or chown -R root / chmod +x on system dirs
_BASH_CHMOD_DANGEROUS_RX = re.compile(
    r"\b(?:chmod|chown)\b[^;|&\n]*\s(?:/|/etc\b|/usr\b|/bin\b|/sbin\b|/lib\b|/var\b|/boot\b)",
    re.IGNORECASE,
)

# git push --force / -f to any branch (blanket ASK; fine-grained remote/branch
# filtering is a Slice 3+ concern)
_BASH_GIT_PUSH_FORCE_RX = re.compile(
    r"\bgit\s+push\b[^;|&\n]*(?:--force\b|--force-with-lease\b|-f\b)",
    re.IGNORECASE,
)

# git reset --hard  (destructive history op)
_BASH_GIT_RESET_HARD_RX = re.compile(
    r"\bgit\s+reset\b[^;|&\n]*--hard\b",
    re.IGNORECASE,
)


# ---------------------------------------------------------------------------
# Path-scope classification
# ---------------------------------------------------------------------------


def _path_is_protected(path: str) -> bool:
    if not path:
        return False
    return any(sub in path for sub in _PROTECTED_PATH_SUBSTRINGS)


def _path_is_outside_repo(path: str) -> bool:
    if not path:
        return False
    if path.startswith("/"):
        return True
    parts = path.split("/")
    if ".." in parts:
        return True
    return False


def _path_is_in_approved(path: str, approved: Tuple[str, ...]) -> bool:
    """Return True iff *path* equals, or is nested under, any approved entry."""
    if not path or not approved:
        return False
    for ap in approved:
        if not ap:
            continue
        ap_clean = ap.rstrip("/")
        if path == ap_clean or path.startswith(ap_clean + "/"):
            return True
    return False


# ---------------------------------------------------------------------------
# bash target-path extraction (for rm -rf scope checks)
# ---------------------------------------------------------------------------


def _extract_bash_non_flag_tokens(cmd: str) -> List[str]:
    """Return non-flag tokens from the first statement of a bash command.

    Pragmatic v0 parser: splits on whitespace, stops at first shell-control
    character (``;``, ``&&``, ``||``, ``|``, ``\n``), drops tokens starting
    with ``-``. Sufficient for ``rm -rf <path>`` target detection; future
    slices can swap in a proper shlex-based parser via new rule rows.
    """
    if not cmd:
        return []
    statement: List[str] = []
    for tok in cmd.split():
        if tok in (";", "&&", "||", "|"):
            break
        # Strip embedded control — "foo;bar" collapses to "foo"
        for sep in (";", "&&", "||", "|"):
            if sep in tok:
                tok = tok.split(sep, 1)[0]
        if not tok:
            continue
        statement.append(tok)
    targets = [t for t in statement[1:] if not t.startswith("-")]
    return targets


def _bash_targets_any_system_root(cmd: str) -> bool:
    for tok in _extract_bash_non_flag_tokens(cmd):
        if tok.rstrip("/") in _SYSTEM_ROOTS:
            return True
        # Also catch "/etc/..." style parents
        if tok.startswith("/"):
            for sys_root in _SYSTEM_ROOTS:
                if sys_root.startswith("/") and tok.startswith(sys_root.rstrip("/") + "/"):
                    return True
    return False


def _bash_targets_outside_approved(
    cmd: str,
    approved: Tuple[str, ...],
) -> bool:
    """Return True iff *any* non-flag target in *cmd* is outside approved scope.

    A target is "outside approved" if it is an absolute path, contains ``..``,
    or is a repo-relative path not nested under any approved entry. If the
    command has no targets after the tool name (e.g. ``rm -rf``), we treat
    that as outside approved — a malformed destructive invocation fails closed.
    """
    targets = _extract_bash_non_flag_tokens(cmd)
    if not targets:
        return True
    for t in targets:
        if _path_is_outside_repo(t):
            return True
        if not _path_is_in_approved(t, approved):
            return True
    return False


