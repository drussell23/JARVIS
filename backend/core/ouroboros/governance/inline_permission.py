"""
InlinePermissionGate — per-tool-call deterministic permission classifier.
========================================================================

Slice 1 of the Inline Permission Prompts arc (CC-parity: "is this OK?"
asked inline, per-tool-call, at the boundary of a mutation).

What this module IS
-------------------

A pure-deterministic classifier that takes structured, operator/data-plane
facts about a proposed tool call and returns one of:

    SAFE  — auto-proceed; no prompt needed.
    ASK   — block the tool call; an inline prompt must be rendered.
    BLOCK — hard refuse; no prompt, no proceed.

The classifier is table-driven. The table (``_RULESET``) is the
*load-bearing artifact* of Slice 1 — future slices add ROWS, never change
the primitive API. "Unknown shapes default to ASK" is enforced by the
final RULE_FALLTHROUGH row plus the decide() function's control flow.

What this module IS NOT
-----------------------

* An LLM. No model calls. Zero network I/O. Microsecond per decision.
* A REPL prompter. Slice 2 renders the inline UI. This module only
  emits the *verdict* that tells the REPL whether to prompt.
* A persistence layer. Remembered-allow entries are queried through a
  read-only :class:`RememberedAllowProvider` protocol. Writes belong to
  Slice 3; Slice 1 only consumes the lookup.
* A replacement for existing gates. :class:`PolicyEngine` still runs
  first; :class:`InlinePermissionGate` runs AFTER and may tighten
  (SAFE→ASK, SAFE→BLOCK, NO_MATCH→ASK) but never loosens a BLOCK.

Manifesto alignment
-------------------

* §1 — execution authority is deterministic. The classifier answers the
  authorization question; any "reason" text generated by the model is
  display-only and is never an input here.
* §5 — Tier 0 deterministic fast-path. Pure code, first-match-wins.
* §6 — additive to the Iron Gate. Never weakens an upstream BLOCK.
* §7 — fail-closed: unknown tool shapes default to ASK; autonomous
  routes (BACKGROUND / SPECULATIVE) that would have prompted a human
  are coerced to BLOCK because there is no human on the console.
* §8 — every verdict carries ``rule_id`` and ``ruleset_version`` so
  postmortems can trace exactly which row fired.

Additive taxonomy expansion
---------------------------

New risk shapes land as NEW ROWS in ``_RULESET`` plus NEW TESTS in
``tests/governance/test_inline_permission.py``. The primitive API
(:class:`InlineGateInput`, :class:`InlineGateVerdict`, :func:`decide`)
is frozen for the lifetime of ``inline_permission.v0``. If a future
taxonomy needs fields the current input cannot express, the bump is a
new ruleset version (``inline_permission.v1``) behind a feature flag —
not a reinterpretation of the v0 API.
"""
