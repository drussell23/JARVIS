def test_inspect_introspection_killswitch(monkeypatch):
    monkeypatch.setenv(_INSPECT_FLAG, "false")
    assert is_inspect_introspection_block_enabled() is False
    src = _runner_body("x = inspect.getmembers(self)")
    result = validate_ast(src)
    # With the inspect rule off, this specific escape is no longer flagged
    # as INSPECT_INTROSPECTION. It has no other banned construct → PASSED.
    assert result.reason is not ValidationFailureReason.INSPECT_INTROSPECTION
    assert result.status is ValidationStatus.PASSED


def test_ctx_mutation_setattr_killswitch(monkeypatch):
    monkeypatch.setenv(_CTX_MUT_FLAG, "false")
    assert is_ctx_mutation_block_enabled() is False
    src = _runner_body('setattr(ctx, "x", 1)')
    result = validate_ast(src)
    assert result.reason is not ValidationFailureReason.CTX_MUTATION
    assert result.status is ValidationStatus.PASSED


def test_ctx_mutation_killswitch_still_allows_attr_form_off(monkeypatch):
    """With the ctx-mutation rule OFF, BOTH the attribute form and the
    setattr form are no longer flagged (single rule, single switch)."""
    monkeypatch.setenv(_CTX_MUT_FLAG, "false")
    src = _runner_body("ctx.phase = 'X'")
    result = validate_ast(src)
    assert result.reason is not ValidationFailureReason.CTX_MUTATION
    assert result.status is ValidationStatus.PASSED


def test_introspection_attr_killswitch_disables_getattribute(monkeypatch):
    monkeypatch.setenv(_INTRO_ATTR_FLAG, "false")
    src = _runner_body('x = object.__getattribute__(object, "y")')
    result = validate_ast(src)
    assert result.reason is not ValidationFailureReason.INTROSPECTION_ESCAPE


