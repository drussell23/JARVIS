_CLEAN_CONTROLS = [
    # --- benign comprehensions ---
    ("list_comp", "x = [i * 2 for i in range(10)]"),
    ("dict_comp", "x = {k: v for k, v in ctx.items.items()}"),
    ("filtered_comp", "x = [self.helper(i) for i in ctx.data if i]"),
    ("genexp_sum", "x = sum(i for i in ctx.nums)"),
    ("nested_comp", "x = [[j for j in row] for row in ctx.matrix]"),
    ("set_comp", "x = {s.strip() for s in ctx.lines}"),
    # --- benign setattr/delattr on NON-ctx objects ---
    ("setattr_self", 'setattr(self, "_cache", {})'),
    ("setattr_obj", "setattr(other_obj, name, val)"),
    ("delattr_self", 'delattr(self, "_temp")'),
    ("setattr_local", 'cfg = object(); setattr(cfg, "k", 1)'),
    # --- benign inspect (NOT-banned functions) ---
    ("inspect_signature", "x = inspect.signature(self.run)"),
    ("inspect_isfunction", "x = inspect.isfunction(self.helper)"),
    ("inspect_getsource", "x = inspect.getdoc(self.run)"),
    # --- normal attribute access + method calls ---
    ("attr_chain", "x = self.foo.bar()"),
    ("list_append", "result = []; result.append(ctx.value)"),
    ("module_call", "import json; x = json.dumps(ctx.data)"),
    # --- normal class instantiation / type checks ---
    ("isinstance_int", "x = isinstance(ctx.value, int)"),
    ("type_equality", "x = (ctx.value == int)"),
    ("instantiate", "x = dict(); x['k'] = ctx.value"),
    # --- getattr on a non-banned attr name ---
    ("getattr_benign", 'x = getattr(self, "name", None)'),
    # --- benign ctx READS (not mutations) ---
    ("ctx_read", "x = ctx.phase; y = ctx.advance(status='ok')"),
]


@pytest.mark.parametrize(
    "label, body", _CLEAN_CONTROLS, ids=[c[0] for c in _CLEAN_CONTROLS]
)
def test_clean_control_not_flagged(label, body):
    src = _runner_body(body)
    result = validate_ast(src)
    assert result.status is ValidationStatus.PASSED, (
        f"FALSE POSITIVE on clean control {label!r}: "
        f"reason={result.reason} detail={result.detail!r}\nsrc:\n{src}"
    )


def test_clean_control_matrix_size():
    """Sanity: the clean-control matrix is broad (>= 20 distinct patterns)."""
    assert len(_CLEAN_CONTROLS) >= 20


def test_realistic_benign_phase_runner_validates_clean():
    """A realistic ~25-line benign PhaseRunner that reads ctx, builds data
    via comprehensions, calls helpers and returns a PhaseResult must VALIDATE
    clean — the load-bearing FP gate."""
    src = textwrap.dedent("""
        from __future__ import annotations
        import json
        import inspect
        from backend.core.ouroboros.governance.phase_runner import (
            PhaseRunner, PhaseResult,
        )
        from backend.core.ouroboros.governance.op_context import (
            OperationContext,
        )

        class EnrichPhaseRunner(PhaseRunner):
            phase = "ENRICH"

            def _normalize(self, items):
                return [i.strip() for i in items if i]

            async def run(self, ctx: OperationContext) -> PhaseResult:
                try:
                    raw = ctx.payload
                    cleaned = self._normalize(raw)
                    counts = {k: len(v) for k, v in ctx.groups.items()}
                    sig = inspect.signature(self._normalize)
                    total = sum(c for c in counts.values())
                    summary = {
                        "cleaned": cleaned,
                        "total": total,
                        "params": list(sig.parameters),
                    }
                    blob = json.dumps(summary)
                    return ctx.advance(status="ok", note=blob)
                except Exception as exc:
                    return ctx.advance(status="fail", reason=str(exc))
    """).strip()
    result = validate_ast(src)
    assert result.status is ValidationStatus.PASSED, (
        f"realistic benign runner FALSE POSITIVE: "
        f"reason={result.reason} detail={result.detail!r}"
    )


